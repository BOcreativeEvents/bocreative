export interface EventData {
    slug: string
    title: string
    category: string
    location: string
    year: string
    img: string           // card thumbnail
    imgPosition?: string  // CSS object-position for card crop (default: center center)
    imgScale?: number     // initial zoom on card thumbnail (default: 1)
    imgRotate?: number    // rotation in degrees (positive = clockwise)
    bgImg?: string        // project page background (falls back to img if not set)
    stripImg?: string     // homepage film strip image (falls back to img if not set)
    stripPosition?: string // CSS object-position for the strip crop
    heroImg: string | null  // expanding center image (null = placeholder)
    gallery: (string | null)[]  // videos + photos
    featuredVideo?: string    // optional full-width feature video shown below reels
    description: string
    storyboard?: string       // optional storyboard/moodboard image
    storyboardCaption?: string
    partnershipBadge?: string  // if set, shows a gold trust badge with custom text
    yearSections?: { year: string; video: string }[]  // multi-year projects: each year gets its own video section
    featuredVideos?: string[]  // multiple full-width videos stacked vertically
}

